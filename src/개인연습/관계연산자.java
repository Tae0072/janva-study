package 개인연습;

public class 관계연산자 {
    public static void main(String[] args) {
        System.out.println((3==4)+"");
        System.out.println((3!=4)+"");
        System.out.println((3>4)+"");
        System.out.println((3<4)+"");

        System.out.println((3==3&&4==7)+"");
        System.out.println((3==3||4==7)+"");
    }
}
